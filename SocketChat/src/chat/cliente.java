    @Override
    public void run() {
        try {
            ServerSocket recibido= new ServerSocket(4242);
            Socket cliente;
            paquete recibidoF;
            while(true){
                cliente= recibido.accept();
                ObjectInputStream entrada= new ObjectInputStream(cliente.getInputStream()); 
                recibidoF= (paquete)entrada.readObject();
                
                txtA_mensajes.append("\n"+recibidoF.getMensaje());
            }
        } catch (Exception e) {
            e.printStackTrace();
        } 
 
    }
